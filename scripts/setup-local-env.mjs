#!/usr/bin/env node
// Regenerates gitignored per-app `.env.local` files for local development.
//
// Why this exists:
// - `packages/next-config/keys.ts` requires NEXT_PUBLIC_APP_URL and
//   NEXT_PUBLIC_WEB_URL, so the Next apps will not even boot without them.
// - The roaster/org/admin portals each use a SEPARATE Clerk application but read
//   the SAME canonical env vars (NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY / CLERK_SECRET_KEY).
//   A single globally-injected secret cannot serve three different apps, and
//   @next/env does NOT let a per-app `.env.local` override a value that already
//   exists in process.env. So we keep the canonical names OUT of the global
//   environment and instead source each portal's keys from DISTINCT names:
//     ROASTER_CLERK_PUBLISHABLE_KEY / ROASTER_CLERK_SECRET_KEY
//     ORG_CLERK_PUBLISHABLE_KEY     / ORG_CLERK_SECRET_KEY
//     ADMIN_CLERK_PUBLISHABLE_KEY   / ADMIN_CLERK_SECRET_KEY
//   and write the canonical names into each app's `.env.local` here.
//
// `.env.local` files are gitignored and do not survive a fresh VM, so this script
// is meant to run on startup (it is idempotent). Secret VALUES persist via the
// Cursor Cloud Secrets panel; this script just re-derives the local files from them.
//
// It intentionally does NOT write DATABASE_URL / STRIPE_* / SESSION_SECRET / RESEND_* /
// UPSTASH_* — those are injected globally and are read straight from process.env.

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");

const PORTS = { web: 3000, roaster: 3001, org: 3002, admin: 3003 };

// Shared, non-secret public URLs (same across apps; used for cross-app links).
const SHARED_PUBLIC = {
  NEXT_PUBLIC_APP_URL: "http://localhost:3000",
  NEXT_PUBLIC_WEB_URL: "http://localhost:3001",
  NEXT_PUBLIC_API_URL: "http://localhost:3002",
  NEXT_PUBLIC_DOCS_URL: "http://localhost:3004",
};

const CLERK_URLS = {
  NEXT_PUBLIC_CLERK_SIGN_IN_URL: "/sign-in",
  NEXT_PUBLIC_CLERK_SIGN_UP_URL: "/sign-up",
  NEXT_PUBLIC_CLERK_AFTER_SIGN_IN_URL: "/",
  NEXT_PUBLIC_CLERK_AFTER_SIGN_UP_URL: "/",
};

// Portals that authenticate with Clerk and the distinct secret prefix to source from.
const CLERK_PORTALS = {
  roaster: "ROASTER",
  org: "ORG",
  admin: "ADMIN",
};

function clean(value) {
  return typeof value === "string" && value.trim() !== "" ? value.trim() : null;
}

function buildEnv(app) {
  const entries = { ...SHARED_PUBLIC };

  const prefix = CLERK_PORTALS[app];
  if (prefix) {
    Object.assign(entries, CLERK_URLS);
    const pub = clean(process.env[`${prefix}_CLERK_PUBLISHABLE_KEY`]);
    const secret = clean(process.env[`${prefix}_CLERK_SECRET_KEY`]);
    if (pub) {
      entries.NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY = pub;
    }
    if (secret) {
      entries.CLERK_SECRET_KEY = secret;
    }
  }

  return entries;
}

function render(entries) {
  const lines = [
    "# AUTO-GENERATED by scripts/setup-local-env.mjs — do not edit by hand.",
    "# Regenerate with: node scripts/setup-local-env.mjs",
    "# Secret values come from the Cursor Cloud Secrets panel (see AGENTS.md).",
    "",
  ];
  for (const [key, value] of Object.entries(entries)) {
    lines.push(`${key}=${JSON.stringify(value)}`);
  }
  lines.push("");
  return lines.join("\n");
}

let clerkConfigured = 0;
for (const app of Object.keys(PORTS)) {
  const entries = buildEnv(app);
  const filePath = resolve(ROOT, "apps", app, ".env.local");
  mkdirSync(dirname(filePath), { recursive: true });
  writeFileSync(filePath, render(entries));

  const hasClerk = Boolean(entries.NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY);
  if (hasClerk) {
    clerkConfigured += 1;
  }
  let clerkNote = "";
  if (CLERK_PORTALS[app]) {
    clerkNote = hasClerk
      ? " (Clerk: configured)"
      : " (Clerk: not set — portal sign-in disabled)";
  }
  console.log(`wrote apps/${app}/.env.local${clerkNote}`);
}

console.log(
  `\nDone. ${clerkConfigured}/${Object.keys(CLERK_PORTALS).length} portals have Clerk keys.`
);
