#!/usr/bin/env node

import { execFile } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const DEFAULT_MANIFEST = "ops/1password/vercel-production-envs.json";
const DEFAULT_OUT_DIR = ".vercel/env";
const VALUE_OPTIONS = new Set(["--manifest", "--out-dir", "--vault", "--project"]);

function printUsage() {
  console.log(`Usage:
  node scripts/render-1password-vercel-envs.mjs [options]

Options:
  --manifest <path>  Manifest file (default: ${DEFAULT_MANIFEST})
  --out-dir <path>   Output directory for generated env files (default: ${DEFAULT_OUT_DIR})
  --vault <name>     Override the vault name from the manifest
  --project <name>   Render one logical project (repeatable: web, roaster, org, admin)
  --check-only       Validate 1Password refs without writing files
  --help             Show this help

Example:
  pnpm vercel:env:render:production
  pnpm vercel:env:render:production -- --project web --check-only
`);
}

function readOptionValue({ arg, argv, index }) {
  const value = argv[index + 1];
  if (!value) {
    throw new Error(`${arg} requires a value`);
  }
  return value;
}

function applyOption({ arg, args, value }) {
  if (arg === "--manifest") {
    args.manifest = value;
    return;
  }

  if (arg === "--out-dir") {
    args.outDir = value;
    return;
  }

  if (arg === "--vault") {
    args.vault = value;
    return;
  }

  args.projects.push(value);
}

function parseArgs(argv) {
  const args = {
    checkOnly: false,
    manifest: DEFAULT_MANIFEST,
    outDir: DEFAULT_OUT_DIR,
    projects: [],
    vault: undefined,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === "--") {
      continue;
    }

    if (arg === "--help") {
      printUsage();
      process.exit(0);
    }

    if (arg === "--check-only") {
      args.checkOnly = true;
      continue;
    }

    if (VALUE_OPTIONS.has(arg)) {
      const value = readOptionValue({ arg, argv, index });
      index += 1;
      applyOption({ arg, args, value });
      continue;
    }

    throw new Error(`Unknown argument: ${arg}`);
  }

  return args;
}

function loadManifest(path) {
  const manifest = JSON.parse(readFileSync(path, "utf8"));

  if (!manifest.vault) {
    throw new Error("Manifest must include vault");
  }

  if (!manifest.items) {
    throw new Error("Manifest must include items");
  }

  if (!manifest.projects) {
    throw new Error("Manifest must include vault, items, and projects");
  }

  return manifest;
}

function encodeEnvValue(value) {
  return JSON.stringify(value);
}

function onePasswordRef({ vault, itemTitle, field }) {
  return `op://${vault}/${itemTitle}/${field}`;
}

async function readOnePasswordField(ref) {
  const { stdout } = await execFileAsync("op", ["read", ref, "--no-newline"], {
    maxBuffer: 1024 * 1024,
  });
  return stdout;
}

function isPlaceholder(value, placeholders) {
  return placeholders.includes(value.trim());
}

async function collectProjectEnv({ manifest, placeholders, projectName, projectConfig, vault }) {
  const entries = [];
  const skippedOptional = [];
  const problems = [];

  for (const [key, source] of Object.entries(projectConfig.vars)) {
    const item = manifest.items[source.item];
    if (!item) {
      problems.push(`${projectName}.${key}: unknown item alias "${source.item}"`);
      continue;
    }

    const ref = onePasswordRef({
      field: source.field,
      itemTitle: item.title,
      vault,
    });

    try {
      const value = await readOnePasswordField(ref);
      if (isPlaceholder(value, placeholders)) {
        if (source.optional) {
          skippedOptional.push(key);
        } else {
          problems.push(`${projectName}.${key}: fill ${ref}`);
        }
        continue;
      }

      entries.push([key, value]);
    } catch {
      if (source.optional) {
        skippedOptional.push(key);
      } else {
        problems.push(`${projectName}.${key}: missing ${ref}`);
      }
    }
  }

  return { entries, problems, skippedOptional };
}

function writeProjectEnv({ entries, env, outDir, projectName }) {
  const filePath = resolve(outDir, `${projectName}.${env}.env`);
  const lines = [
    "# Generated by scripts/render-1password-vercel-envs.mjs",
    "# Do not edit directly; update 1Password and re-render.",
    ...entries.map(([key, value]) => `${key}=${encodeEnvValue(value)}`),
    "",
  ];

  mkdirSync(outDir, { recursive: true });
  writeFileSync(filePath, lines.join("\n"));
  return filePath;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const manifestPath = resolve(process.cwd(), args.manifest);
  const outDir = resolve(process.cwd(), args.outDir);
  const manifest = loadManifest(manifestPath);
  const vault = args.vault ?? manifest.vault;
  const placeholders = manifest.placeholderValues ?? ["", "__SET_ME__"];
  const projectNames = args.projects.length ? args.projects : Object.keys(manifest.projects);
  const allProblems = [];

  for (const projectName of projectNames) {
    const projectConfig = manifest.projects[projectName];
    if (!projectConfig) {
      throw new Error(
        `Unknown project "${projectName}". Expected one of: ${Object.keys(manifest.projects).join(", ")}`
      );
    }

    const { entries, problems, skippedOptional } = await collectProjectEnv({
      manifest,
      placeholders,
      projectConfig,
      projectName,
      vault,
    });

    if (problems.length > 0) {
      allProblems.push(...problems);
      continue;
    }

    if (!args.checkOnly) {
      writeProjectEnv({
        entries,
        env: projectConfig.env,
        outDir,
        projectName,
      });
    }

    const skipped = skippedOptional.length ? ` (skipped optional: ${skippedOptional.join(", ")})` : "";
    console.log(
      `${projectName}: ${args.checkOnly ? "validated" : "rendered"} ${entries.length} vars${skipped}`
    );
  }

  if (allProblems.length > 0) {
    console.error("");
    console.error("1Password values are missing or still placeholders:");
    for (const problem of allProblems) {
      console.error(`- ${problem}`);
    }
    process.exit(1);
  }
}

main().catch((error) => {
  console.error("");
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
